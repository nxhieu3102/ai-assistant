/**
 * Get translation for a key based on selected language
 */
export const getTranslation = (key: TranslationKey, language: SupportedLanguage = 'English'): string => {
  const translations = UI_TRANSLATIONS[language] || UI_TRANSLATIONS.English
  return translations?.[key] || UI_TRANSLATIONS.English?.[key] || key
}

/**
 * Hook for using translations in React components
 */
export const useTranslation = (defaultLanguage: SupportedLanguage = 'English') => {
  return (key: TranslationKey): string => getTranslation(key, defaultLanguage)
}
